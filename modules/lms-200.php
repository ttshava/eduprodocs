<!-- Offline Delivery -->
<section class="section">
  <div class="container">
    <h2 class="heading">How Offline Delivery Works</h2>
    <p class="subheading">This is the defining feature of Edupro's Moodle integration — a full-featured LMS serving hundreds of students simultaneously with zero internet dependency.</p>
    <div class="how-it-works">
      <div class="step">
        <div class="step-number">1</div>
        <div class="step-content">
          <h3>Moodle on Local Apache Server</h3>
          <p>Edupro installs Moodle on the school's Edupro server (a rugged, UPS-backed local server). Moodle runs on Apache HTTP Server + MySQL/MariaDB — exactly like a live website, but hosted inside your school building.</p>
        </div>
      </div>
      <div class="step">
        <div class="step-number">2</div>
        <div class="step-content">
          <h3>Content Pre-Loaded at Setup</h3>
          <p>During the school setup visit, Edupro technicians pre-load all video resources, PDF textbooks, and starter course content onto the server. Teachers then add their own materials using the school's local WiFi or LAN from their classroom.</p>
        </div>
      </div>
      <div class="step">
        <div class="step-number">3</div>
        <div class="step-content">
          <h3>Students Access via Local WiFi</h3>
          <p>Students open any web browser on any school computer, tablet, or personal smartphone connected to the school's WiFi and navigate to the school's Moodle URL (e.g., http://192.168.1.10/moodle). No internet required — all content loads instantly from the local network.</p>
        </div>
      </div>
      <div class="step">
        <div class="step-number">4</div>
        <div class="step-content">
          <h3>Cloud Sync When Available</h3>
          <p>When the school has internet connectivity, course completions, grades, and new teacher-uploaded materials optionally sync to the ZimHPC cloud backup. This protects data and allows school proprietors to monitor learning activity remotely.</p>
        </div>
      </div>
    </div>
  </div>
</section>

