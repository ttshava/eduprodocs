const closeNav = () => { mobileNav.classList.remove('open'); backdrop.classList.remove('open'); hamburger.setAttribute('aria-expanded','false'); document.body.style.overflow=''; };
if (hamburger) hamburger.addEventListener('click', openNav);
if (mobileClose) mobileClose.addEventListener('click', closeNav);
if (backdrop) backdrop.addEventListener('click', closeNav);

// Scroll to top
const scrollBtn = document.getElementById('scrollTop');
if (scrollBtn) {
  window.addEventListener('scroll', () => scrollBtn.classList.toggle('visible', window.scrollY > 400));
  scrollBtn.addEventListener('click', () => window.scrollTo({ top: 0, behavior: 'smooth' }));
}

// Tab functionality
document.querySelectorAll('.tab-btn').forEach(btn => {
  btn.addEventListener('click', function() {
    const g = this.dataset.group, t = this.dataset.tab;
    document.querySelectorAll(`.tab-btn[data-group="${g}"]`).forEach(b => b.classList.remove('active'));
    document.querySelectorAll(`.tab-panel[data-group="${g}"]`).forEach(p => p.classList.remove('active'));
    this.classList.add('active');
    const panel = document.querySelector(`.tab-panel[data-tab="${t}"][data-group="${g}"]`);
    if (panel) panel.classList.add('active');
  });
});

